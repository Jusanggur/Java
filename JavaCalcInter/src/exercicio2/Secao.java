package exercicio2;

public class Secao {

	private String secao, conteudo;

	public Secao(String secao, String conteudo) {
		this.secao = secao;
		this.conteudo = conteudo;
	}

	public String getSecao() {
		return secao;
	}

	public void setSecao(String secao) {
		this.secao = secao;
	}

	public String getConteudo() {
		return conteudo;
	}

	public void setConteudo(String conteudo) {
		this.conteudo = conteudo;
	}
	
}
